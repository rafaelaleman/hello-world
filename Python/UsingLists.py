# using lists

# declare the list
a = []

# appending to a list
a.append("rafael")
a.append("maria")
a.append("miguel")
a.append("gabriel")

# printing the list
print(a)

# printing one element of the list
print(a[0])

for element in a:
    print(element)

# adding different types to a list
b = []
b.append("rafael")
b.append(1)
b.append("aleman")
print(b)
